package org.elastos.cordova.plugins.intent;

import org.apache.cordova.CallbackContext;
import org.json.JSONObject;

public class IntentInfo {

    public static final int JWT = 1;
    public static final int URL = 2;

    public static final String REDIRECT_URL = "redirecturl";
    public static final String CALLBACK_URL = "callbackurl";

    String action; // Full action as given by the dapp, including domain and action
    String params;
    long intentId;
    CallbackContext callbackContext;

    String responseJwt = null; // JWT output generated by the dapp or service that has handled the intent.
    String originalJwtRequest = null;
    String redirecturl = null;
    String callbackurl = null;
    String aud = null;
    String req = null;
    int type = URL;


    public IntentInfo(String action, String params, CallbackContext callbackContext) {
        this.action = action;
        this.params = params;
        this.intentId = System.currentTimeMillis(); //Need check the same time action?
        this.callbackContext = callbackContext;
    }
}
